Map::Map() {
    dirt = TextureMng::load_texture("assets/dirt.png");
    grass = TextureMng::load_texture("assets/grass.png");
    water = TextureMng::load_texture("assets/water.png");

    load_map(lvl_one);

    src.x = src.y = 0;
    src.w = dest.w = 32;
    src.h = dest.h = 32;
    dest.x = dest.y = 0;
}

Map::~Map() {
}

void Map::load_map(int lvl[20][25]) {
    
    for(int row = 0; row < 20; ++row) {
        for(int col = 0 ; col < 25; ++col) {
            map[row][col] = lvl[row][col];
        }
    }
}

void Map::draw_map() {
    int type = 0;

    for (int row = 0; row < 20; ++row)
    {
        for (int col = 0; col < 25; ++col)
        {
            type = map[row][col];
            dest.x = col * 32;
            dest.y = row * 32;
            switch(type) {
                case 0:
                    TextureMng::draw_tile(water, src, dest);
                    break;
                case 1:
                    TextureMng::draw_tile(grass, src, dest);
                    break;
                case 2:
                    TextureMng::draw_tile(dirt, src, dest);
                default:
                    break;
            }
                
        }
    }
}